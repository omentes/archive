#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*new;

	i = 0;
	while (src[i] != '\0')
		i++;
	new = (char*)malloc(i + 1);
	i = 0;
	while (src[i] != '\0')
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
