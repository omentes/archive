double	ft_dsqrt(int nb, double delta)
{
	double	a;
	double	b;

	a = 1;
	b = nb;
	if (nb < 0)
		return (0);
	while (((a > b) && (a - b >= delta)) || ((a < b) && (b - a >= delta)))
	{
		a = (a + b) / 2;
		b = nb / a;
	}
	return ((a + b) / 2);
}
