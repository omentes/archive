char	*ft_strcat(char *s1, char const *s2)
{
	int		i;
	int		s1len;

	s1len = 0;
	while (s1[s1len])
		s1len++;
	i = -1;
	while (s2[++i])
		s1[s1len + i] = s2[i];
	s1[s1len + i] = '\0';
	return (s1);
}
