#include "libft.h"

char	*ft_strncat(char *s1, char const *s2, size_t n)
{
	size_t	i;
	int		s1len;

	s1len = ft_strlen((char*)s1);
	i = 0;
	while (s2[i] && i < n)
	{
		s1[s1len + i] = s2[i];
		i++;
	}
	s1[s1len + i] = '\0';
	return (s1);
}
