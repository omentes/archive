#include "libft.h"

_Bool	is_number(char *str)
{
	int i;

	i = 0;
	if (str[0] == '+' || str[0] == '-')
		i++;
	while (str[i])
		if (!(str[i] >= '0' && str[i] <= '9'))
			break ;
		else
			i++;
	if (str[i])
		return (0);
	return (1);
}
